    @Override
    public void error(Marker marker, String msg, Throwable throwable) {
        String message = this.placeholderHelper.replacePlaceholders(msg,null);

        LoggingEvent event =  createLoggingEvent(marker,throwable,null,message);
        event.setMessage(message);
        callAppenders(event);
    }

    private LoggingEvent createLoggingEvent(Marker marker, Throwable throwable, Object[] argumentArray, String message){
        return new LoggingEvent(marker,throwable,argumentArray,message);
    }

    private void callAppenders(ILoggingEvent event){
        pool.execute(()->{
            for(Appender appender : appenders){
                appender.doAppend(event);
            }
        });
    }

